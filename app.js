var main = document.getElementById("main");
var brand = document.getElementById("brand");
var model = document.getElementById("model");
brand.innerHTML += `<option>SELECT BRAND</option>`; 
model.innerHTML += `<option>SELECT MODEL</option>`; 

function renderAllCars(){
    for(var key in carlist){
        // console.log(key)
        brand.innerHTML += `<option value='${key}'>${key}</option>`;
        for(var key1 in carlist[key]){
            // console.log(key1)
                var obj = carlist[key][key1]
                var colorDiv = "";
                for(var i=0; i<obj.color.length; i++){
                    colorDiv += `<div class='${obj.color[i]}'>
                    </div>`
                }
                main.innerHTML += `<div class="col-12 col-sm-12 col-md-6 col-lg-3 mb-2">
                <div class="card" style="width: 100%;">
                    <img src="${obj.image}" class="card-img-top" alt="...">
                    <div class="card-body">
                      <h5 class="card-title">${key1.toUpperCase()} (${obj.model})</h5>
                      <p class="card-text">${obj.Mileage}
                      <br/>
                      ${obj.Transmission}
                      </p>
                      <h6>${obj.price}<h6/>
                      <div class="d-flex gap-2">
                        ${colorDiv}
                      </div>
    
                    </div>
                  </div>
            </div>`
            
        }
    }
}
renderAllCars()

function setModel(){
    // console.log("brand =>", carlist[brand.value]);
    model.innerHTML = "";
    model.innerHTML += `<option>SELECT MODEL</option>`;     
    for(var key in carlist[brand.value]){
        model.innerHTML += `<option value='${key}'>${key}</option>`;

    }
}

